package com.example.voyproject.AddFood;

public class ListData {
    String name;
    int kcal;
    float protein, fat, carbohydrat;

    public ListData(String name, int kcal, float protein, float fat, float carbohydrat)
    {
        this.name = name;
        this.kcal = kcal;
        this.protein = protein;
        this.fat = fat;
        this.carbohydrat = carbohydrat;
    }

}
